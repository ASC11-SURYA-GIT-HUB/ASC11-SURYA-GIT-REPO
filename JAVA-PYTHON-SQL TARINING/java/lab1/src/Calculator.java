class Calculator {

    public int add(int a, int b) {
        return a + b;
    }

    public int add(int a, int b, int c) {
        return a + b + c;
    }

    public double add(double a, double b) {
        return a + b;
    }

    public String add(String str, int a) {
        return str + " " + a;
    }

    public int add(short a, short b) {
        return a + b;
    }
}
